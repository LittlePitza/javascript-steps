// A JavaScript variable is a named container that can store data values, which can be retrieved or reassigned later.
// Example of a variable declaration using 'let':
// let myVariable = "Hello, World!";

// Declaring variables to store the first and last name using the 'let' keyword.
let firstName = 'Edgar'; // 'firstName' stores the string 'Edgar'.
let lastName = ' Martinez'; // 'lastName' stores the string ' Martinez'.

// Concatenating the 'lastName' and 'firstName' variables and logging the result to the console.
console.log(lastName + firstName);

// Declaring a constant variable using 'const'.
// Constant variables cannot be reassigned after their initial assignment.
const HELLOMESSAGE = 'Hi! '; // 'HELLOMESSAGE' stores the string 'Hi! '.

// Logging a concatenated message using 'HELLOMESSAGE', 'firstName', and 'lastName'.
console.log(HELLOMESSAGE + firstName + lastName);

// Primitive Types
// Declaring variables to store data of different primitive types in JavaScript.

// String type: 'favoriteFood' stores the string 'berrys'.
let favoriteFood = 'berrys';

// String type: 'favoriteCandy' stores the string 'Chocolate'.
let favoriteCandy = "Chocolate";

// Number type: 'numerofDonuts' stores the integer 12.
let numerofDonuts = 12;

// Number type: 'pi' stores the floating-point number 3.14159.
let pi = 3.14159;

// BigInt type: 'veryLargeNumber' stores a large integer value using the BigInt literal 'n'.
let veryLargeNumber = 9007199254740991n;

// Boolean type: 'lovesCoffee' stores the boolean value 'true'.
let lovesCoffee = true;

// Logging each variable to the console to display its value.
console.log(favoriteFood); // Logs 'berrys'.
console.log(favoriteCandy); // Logs 'Chocolate'.
console.log(numerofDonuts); // Logs 12.
console.log(pi); // Logs 3.14159.
console.log(veryLargeNumber); // Logs 9007199254740991n.
console.log(lovesCoffee); // Logs true.
